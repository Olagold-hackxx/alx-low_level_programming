#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: int to be added
 * @b: int to be added
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtrat numbers
 * @a: int to be subtracted
 * @b: int to be subtracted
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: int to be multiplied
 * @b: int to be multiplied
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide numbers
 * @a: int to be divided
 * @b: int to be divided
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - get numbers remainder
 * @a: int to be divided
 * @b: divisor
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
